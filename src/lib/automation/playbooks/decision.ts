/**
 * Biblioteca de Automation Playbooks de DECISIONES INTELIGENTES (Fase E1.10). No
 * integra IA: instala automatizaciones que TOMAN DECISIONES usando la
 * arquitectura desacoplada `src/lib/decision` (Decision Engine → Recommendation
 * Engine → Prediction → Optimization, servida por Decision Providers). Hoy el
 * único proveedor es `rule_based`; mañana se añade un AI/ML Provider sin tocar
 * estos playbooks ni el resto del sistema, y las empresas nunca ven "IA": solo
 * funcionalidades inteligentes.
 *
 * Regla de oro: el Decision Engine DECIDE (paso "Decidir", vía `INVOKE_MODULE`
 * al módulo `decision`); el Action Engine EJECUTA (paso "Ejecutar"). Nunca al
 * revés. Toda decisión pasa por el Decision Engine, toda recomendación por el
 * Recommendation Engine y toda acción por el Action Engine.
 *
 * Cada Playbook es una automatización real (`config`) + su documentación
 * completa (24 apartados, incl. "Decision Provider utilizado", "Acciones
 * sugeridas" y "Acciones ejecutadas"). Universal por diseño; nada hardcodeado.
 */

