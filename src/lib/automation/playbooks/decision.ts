// KPIs típicos de decisiones/recomendaciones.
const DEC_KPIS = [
  'precision_recomendacion', 'aceptacion_recomendacion', 'conversion_recomendada',
  'oportunidades_detectadas', 'riesgos_detectados', 'churn_evitado',
  'renovaciones_previstas', 'roi_por_decision', 'acciones_priorizadas', 'cobertura',
] as const

/** Helper: completa los apartados comunes de un playbook de decisiones. */
function pb(
  p: Omit<AutomationPlaybook, 'category' | 'editable' | 'kpis' | 'engines' | 'decisionProvider'> &
    Partial<Pick<AutomationPlaybook, 'engines' | 'kpis' | 'editable' | 'decisionProvider'>>,
): AutomationPlaybook {
  return {
    category: 'decisiones',
    editable: p.editable ?? EDITABLE,
    kpis: p.kpis ?? DEC_KPIS,
    engines: p.engines ?? ['rule', 'action', 'analytics'],
    decisionProvider: p.decisionProvider ?? 'rule_based',
    ...p,
  }
}

// Paso reutilizable: "Decidir" (Decision Engine) — NO ejecuta acciones.
function decideStep(kind: string, label = 'Decidir') {
  return {
    label,
    actions: [
      { type: ACTION_TYPES.INVOKE_MODULE, params: { module: 'decision', action: kind } },
      { type: ACTION_TYPES.RECORD_EVENT, params: { event: 'decision.tomada', kind } },
    ],
    chain: { event: EV.DECISION_MADE },
  }
}

