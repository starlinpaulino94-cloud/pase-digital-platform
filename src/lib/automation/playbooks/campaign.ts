/**
 * Biblioteca de Automation Playbooks de CAMPAÑAS (Fase E1.8). No es un sistema de
 * notificaciones: es un CAMPAIGN ORCHESTRATION FRAMEWORK. Cada campaña coordina
 * VARIOS motores de MembeGo (Promotion, Membership, Benefit, Reward, Referral,
 * Gamification, Automation, Analytics) para ejecutar una estrategia comercial
 * completa: evento → Rule Engine → segmentación → selección de estrategia →
 * promociones/beneficios/recompensas/puntos/misiones/cupones/automatizaciones/
 * campañas hijas → notificaciones → medición → optimización.
 *
 * La orquestación se hace siempre por eventos, reglas y acciones reutilizables
 * (Action Engine `INVOKE_MODULE`/`RUN_WORKFLOW`/`CREATE_EVENT`), nunca con lógica
 * aislada. Estrategias probadas de orquestación (Braze Canvas/Salesforce Journey/
 * Klaviyo Flows) convertidas en playbooks UNIVERSALES instalables sobre el
 * Automation Engine (E1). Car Wash es solo la primera industria que las usa.
 *
 * Cada Playbook es una automatización real (`config`) + su documentación
 * completa (25 apartados, incl. "Membresías compatibles" y "Gamificación
 * compatible"). Nada hardcodeado por industria: todo por variable/código.
 */

