// Configuración editable mínima (Documento Maestro): común a todos.
const EDITABLE = [
  'nombre', 'descripcion', 'triggers', 'reglas', 'segmentos', 'fechas', 'horarios',
  'duracion', 'prioridad', 'presupuesto', 'variables', 'beneficios', 'promociones',
  'membresias', 'canales', 'idioma', 'sucursales', 'limites',
] as const

// KPIs típicos de campañas orquestadas.
const CAMP_KPIS = [
  'participacion', 'conversion', 'beneficios_utilizados', 'ingresos', 'roi',
  'coste', 'alcance', 'ticket_promedio', 'segmentos_impactados', 'motores_activados',
  'tasa_apertura', 'tasa_clic',
] as const

// Mecánicas de gamificación reutilizables (referencia, no hardcode).
const GAMI = ['puntos', 'xp', 'niveles', 'misiones', 'rachas', 'insignias', 'ranking'] as const

/** Helper: completa los apartados comunes de un playbook de campaña. */
function pb(
  p: Omit<AutomationPlaybook, 'category' | 'editable' | 'kpis' | 'engines' | 'compatibleGamification'> &
    Partial<Pick<AutomationPlaybook, 'engines' | 'kpis' | 'editable' | 'compatibleGamification'>>,
): AutomationPlaybook {
  return {
    category: 'campanas',
    editable: p.editable ?? EDITABLE,
    kpis: p.kpis ?? CAMP_KPIS,
    engines: p.engines ?? ['rule', 'action', 'campaign', 'analytics'],
    compatibleGamification: p.compatibleGamification ?? [...GAMI],
    ...p,
  }
}

