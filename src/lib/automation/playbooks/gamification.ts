const U = I.UNIVERSAL

// Configuración editable mínima (Documento Maestro): común a todos.
const EDITABLE = [
  'nombre', 'descripcion', 'triggers', 'reglas', 'objetivos', 'niveles',
  'recompensas', 'variables', 'segmentos', 'horarios', 'fechas', 'duracion',
  'prioridad', 'limites', 'idioma', 'sucursales',
] as const

// KPIs típicos de gamificación conductual (siempre atados a negocio).
const GAM_KPIS = [
  'frecuencia_visitas', 'misiones_completadas', 'rachas_activas', 'retencion',
  'ltv', 'uso_beneficios', 'referidos_generados', 'renovaciones',
  'engagement', 'conversion', 'participacion_retos', 'ranking_actividad',
] as const

// Tipos de recompensa reutilizables (referencia, no hardcode).
const REWARDS = ['puntos', 'xp', 'insignias', 'niveles', 'beneficios', 'cupones', 'promociones', 'accesos', 'membresias'] as const

/** Helper: completa los apartados comunes de un playbook de gamificación. */
function pb(
  p: Omit<AutomationPlaybook, 'category' | 'editable' | 'kpis' | 'engines' | 'compatibleRewards'> &
    Partial<Pick<AutomationPlaybook, 'engines' | 'kpis' | 'editable' | 'compatibleRewards'>>,
): AutomationPlaybook {
  return {
    category: 'gamificacion',
    editable: p.editable ?? EDITABLE,
    kpis: p.kpis ?? GAM_KPIS,
    engines: p.engines ?? ['rule', 'action', 'gamification', 'analytics'],
    compatibleRewards: p.compatibleRewards ?? [...REWARDS],
    ...p,
  }
}

