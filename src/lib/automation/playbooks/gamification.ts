/**
 * Biblioteca de Automation Playbooks de GAMIFICACIÓN CONDUCTUAL (Fase E1.9). No
 * es "solo puntos/insignias/rankings": es un sistema de CAMBIO DE COMPORTAMIENTO
 * al servicio de objetivos comerciales (frecuencia, visitas, consumo, referidos,
 * renovaciones, uso de beneficios, hábito, retención, LTV). La gamificación
 * nunca existe por entretenimiento: cada mecánica impulsa una meta de negocio.
 *
 * Se integra de forma nativa con el resto de motores: una misión puede activar
 * promociones, una racha otorgar beneficios, un logro iniciar campañas y un nivel
 * desbloquear membresías — todo por eventos/reglas/acciones reutilizables
 * (`INVOKE_MODULE`/`RUN_WORKFLOW`/`APPLY_BENEFIT`/chaining). Los NIVELES son
 * completamente configurables: la arquitectura no depende de nombres concretos
 * (Bronce/Plata/…); se referencian por variable.
 *
 * Estrategias probadas (Duolingo/Strava/Starbucks/Nike Run Club/videojuegos)
 * convertidas en playbooks UNIVERSALES instalables sobre el Automation Engine
 * (E1). Car Wash es solo la primera industria que las usa.
 *
 * Cada Playbook es una automatización real (`config`) + su documentación
 * completa (25 apartados, incl. "Recompensas compatibles"). Nada hardcodeado por
 * industria ni por nivel: todo por variable/código.
 */

